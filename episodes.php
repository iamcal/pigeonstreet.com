	<a name="episode1"></a>
	<b>1. All In A Day's Work</b><br>
	<br>
	First aired: February 10th, 1981<br>
	<br>
	It is Saturday. A pigeon is looking for Bob but he is nowhere to be seen. He is not even down at his Bike shop. Eventually the Pigeon spies him feeding his Pigeons in the abck yard. Reg is not working in the Park today. Both he and his wife Doreen are wearing their dancing outfits. They want to win another cup and so are practising their ballroom moves. At first it looks as though they need a lot of practise.<br>
	<br>
	Daisy is feeding the pigeons on her window box but they are soon frightend off by Rose who does not like pigeons. Dr Glossdop has a day off and is busy doing some decorating. Mrs Glossop is making some jam. Polly and Molly are helping their mum with the jam jars when they spy Gerald's tropical fish who does not look well at all. Gerald has gone to Mr Macadoo's pet shop to get some medicine. Mr Baskerville is also keen to go and see Mr Macadoo and his dog Watson is a bit under the weather. He lices in skyrise court a block of flats at the 13th floor. the lift is broken - what will he do.<br>
	<br>
	William and his mate Jim come to the rescue. They take mr baskerville and watson down in the window crane. On the way they pick up other bits and bobs for other people trapped by the broken lift. Mrs Baker wants a letter posted. William decides that there is too much work to be done by only a few people so gets one of the pigoens to deliver a message to Bob. Bob gets Clara and her van and the rest of the people in pigeon street to help. They deliver groceries, letters, a new clock, the milk and even help a mother and baby down 11 floors.<br>
</div>

