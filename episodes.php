<div class="boxout">
	<a name="episode3"></a>
	<b>3. Pigeon At Sea</b><br>
	<br>
	First aired: February 24th, 1981<br>
	<br>
	Reg and Gerald finds a pigeon that must be rescued from drowning after trying to swim with the ducks. They take it to Macadoo, who suggests that Mr. Baskerville might be able to talk sense to it. Mr Baskerville succeeds partly - the pigeon no longer thinks it is a duck, it decides to be a seagull instead. Bob, however, gets Clara to take the pigeon to the ocean. After attempting to catch fish in the rough sea, the bird is finally content to be a pigeon.<br>
</div>

