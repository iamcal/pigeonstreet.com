<b>Other Items</b><br>
<br>
What has to be the rarest item is a white label test pressing of a 2m 32s version of Pigeon Street theme (performed by Soulyard - one side pressed only). **Please note that this was severely mispressed and has a very tinny-distorted playback). It has a plain "bubblegum" white label with just a handwritten number (# 013) on the front with Pigeon Street scribbled on it in smudged biro. I was given this ten years ago on a school trip to BBC Pebble Mill by the person in charge of the Music Library there. I have never seen another one since - nor will I ever see one again??? <i>--Steve May</i><br>
