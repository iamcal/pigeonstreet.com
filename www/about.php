<h1>Pigeon Street?</h1>
<br>

Pigeon Street was first shown on BBC1 / BBC2 in 1981.<br>
<br>
It was a series of animated adventures each lasting about 10 minutes.<br>
<br>
The adventures concerned the people and animals of Pigeon Street.<br>
<br>
<br>

<b>Airings</b><br>
<br>
There were <a href="episodes.php">two seasons</a> which were originally aired from February 10th to March 31th 1981 and November 19th to December 17th 1981.<br>
<br>
Both seasons were then repeated in 1983 &amp; 1985 and various episodes in 1984 &amp; 1986.<br>
<br>
Both seasons were shown by the BBC a few times between 1986 and 1994, but details are sketchy - In 1993 it was shown at 6.30am on BBC1.<br>
<br>
Seasons one and two were repeated on BBC Choice, starting 12th June 2000.<br>
<br>
<br>

<b>The real Pigeon Street</b><br>
<br>
There are real life Pigeon Street's in <a href="http://www.streetmap.co.uk/streetmap.dll?grid2map?384954&398231" target="_blank">Manchester</a> and <a href="http://www.streetmap.co.uk/streetmap.dll?grid2map?549597&316690" target="_blank">Wisbech</a>. It's unclear if they inspired the programme.<br>
